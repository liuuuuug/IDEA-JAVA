/**
 * 嵌套循环
 * 训练
 * "\t"制表符
 *
 */
public class TestQiantao3 {
    public static void main(String[] args) {


        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print(i + "\t");
            }
            System.out.println();//转行
        }

    }
}